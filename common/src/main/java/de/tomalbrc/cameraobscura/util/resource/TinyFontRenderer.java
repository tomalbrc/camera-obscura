    private static void drawChar(BufferedImage img, char ch, int x, int y, int color) {
        int[] raster = ((DataBufferInt) img.getRaster().getDataBuffer()).getData();
        int width = img.getWidth();

        int base = ch * 5;
        if (base >= FONT.length) return;

        for (int row = 0; row < 5; row++) {
            int mask = FONT[base + row];
            for (int col = 0; col < 5; col++) {
                if ((mask & (1 << (4 - col))) != 0) {
                    int px = x + col;
                    int py = y + row;
                    if (px >= 0 && px < img.getWidth() && py >= 0 && py < img.getHeight()) {
                        raster[py * width + px] = color;
                    }
                }
            }
        }
    }

    public static void drawString(BufferedImage img, String text, int x, int y, int color) {
        int cursorX = x;
        for (char ch : text.toCharArray()) {
            drawChar(img, ch, cursorX, y, color);
            cursorX += 6;
        }
    }
